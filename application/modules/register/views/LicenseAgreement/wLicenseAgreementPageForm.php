                        <div class="xParagraph">4.1 สิทธิในการเก็บ ใช้ข้อมูลของผู้ใช้บริการ ผู้ใช้บริการตกลงให้ความยินยอมบริษัทในการเก็บรวบรวมและประมวลผล ข้อมูลใด ๆ ของผู้ใช้บริการที่มีอยู่ใน AdaPos+ โดยบริษัทมีสิทธิใช้ข้อมูลการใช้งานและข้อมูลอื่นใดที่เกี่ยวกับผู้ใช้บริการ อาทิเช่น รายการสินค้า รายการขาย ฯลฯ เพื่อใช้ในการวิเคราะห์ข้อมูลและปรับปรุงการให้บริการ AdaPos+, บริษัทมีสิทธิเปิดเผยข้อมูลบางส่วนของผู้ใช้บริการกับบุคคลที่สาม อันเป็นไปเพื่อสนับสนุนบริการที่ดียึ่งขึ้น ของ AdaPos+ โดยอาจไม่แจ้งให้ผู้ใช้บริการทราบล่วงหน้า</div>
