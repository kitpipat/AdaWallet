                        <div class="xParagraph">4.2 สิทธิในการเก็บและใช้ข้อมูลการซื้อขาย หากผู้ใช้บริการซื้อ/ซื้อเพิ่ม ออนไลน์ผ่านทาง AdaPos+ ผู้ใช้บริการตกลง รับทราบ และให้ความยินยอมแก่บริษัทในการเก็บข้อมูลการเรียกเก็บเงิน เบอร์โทรศัพท์ และบัตรเครดิตของผู้ใช้บริการ ซึ่งข้อมูลนี้จะถูกใช้ร่วมกับบริษัทบุคคลที่สามที่ให้บริการประมวลผลการเก็บเงิน (เช่น ธนาคาร ผู้ออกบัตรเครดิต หรือ บริษัทโทรศัพท์มือถือ) ทั้งนี้ข้อมูลที่สำคัญของผู้ใช้บริการ (เช่น หมายเลขบัตรเครดิต) จะได้รับการรักษาความปลอดภัยอย่างดีที่สุด</div>
                        <div class="xParagraph">4.3 สิทธิในการโอนสิทธิการใช้ข้อมูลแก่ธุรกิจในเครือ บริษัทอาจโอนสิทธิการใช้ข้อมูลต่าง ๆ ใน AdaPos+ นี้ของบริษัทให้แก่บริษัทแม่ บริษัทย่อย บริษัทร่วมทุน หรือบริษัทอื่นที่เป็นคู่ค้ากันกับบริษัท ภายใต้เงื่อนไขที่จะระบุในอนาคต</div>
                        <div class="xParagraph">4.4 เมื่อผู้ใช้บริการลงทะเบียนเพื่อใช้บริการ AdaPos+ ถือว่าผู้ใช้บริการได้ให้ความยินยอมและตกลงให้บริษัทมีสิทธิเก็บ ใช้ และเปิดเผยข้อมูลใด ๆ ของผู้ใช้บริการ ตามที่กำหนดไว้ในข้อกำหนดและเงื่อนไขการให้บริการฉบับนี้</div>
